char	*ft_strlowcase(char *str)
{
	int i;

	i = 0;
	while (1)
	{
		if (str[i] == '\0')
			break ;
		else
		{
			if (65 <= str[i] && str[i] <= 90)
			{
				str[i] += 32;
				i++;
			}
			else
				i++;
		}
	}
	return (str);
}
