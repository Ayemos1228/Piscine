char	*ft_strupcase(char *str)
{
	int i;

	i = 0;
	while (1)
	{
		if (str[i] == '\0')
			break ;
		else
		{
			if (97 <= str[i] && str[i] <= 122)
			{
				str[i] -= 32;
				i++;
			}
			else
				i++;
		}
	}
	return (str);
}
