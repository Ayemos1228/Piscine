int	ft_str_is_alpha(char *str)
{
	int flag;
	int i;

	flag = 1;
	i = 0;
	while (str[i] != '\0')
	{
		if ((65 <= str[i] && str[i] <= 90) || (97 <= str[i] && str[i] <= 122))
			i++;
		else
		{
			flag = 0;
			break ;
		}
	}
	return (flag);
}
